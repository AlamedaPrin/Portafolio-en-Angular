export class Persona{

    fullName:string;
    email:string;
    position:string;
    ubication:string;
    image:string;

    constructor(fullName:string, email:string, position:string, ubication:string, image:string) 
    {

        this.fullName=fullName;
        this.email=email;
        this.position=position;
        this.ubication=ubication;
        this.image=image;
    }
}